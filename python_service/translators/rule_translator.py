    def translate(self, gloss_sequence):
        """
        翻译手势序列
        gloss_sequence: ["我", "想", "吃饭"]
        """
        if not gloss_sequence:
            return ""

        # 转为字符串
        text = " ".join(gloss_sequence)

        # 1. 检查固定短语
        if text in self.phrases:
            return self.phrases[text]

        # 2. 检查动态模板
        for pattern, template in self.templates:
            match = re.match(pattern, text)
            if match:
                obj = match.group(1)
                return template.format(obj)

        # 3. 默认翻译：组合词汇
        result = " ".join(gloss_sequence)

        # 添加标点
        if any(q in result for q in self.question_words):
            result += "？"
        elif len(result) < 5:
            result += "！"
        else:
            result += "。"

        return result

    def translate_with_context(self, gloss_sequence, history=None):
        """带上下文的翻译"""
        result = self.translate(gloss_sequence)

        # 简单的上下文处理
        if history and history[-1].endswith("？"):
            # 如果是回答上一个问题
            result = result.replace("？", "").replace("。", "")
            result = "是的，" + result

        return result


